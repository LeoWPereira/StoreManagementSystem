    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
        // TODO add your handling code here:
        if(BuscarCliente.isEnabled() == false && BuscarVeiculo.isEnabled() == false)
        {
            CtrlProduto ctrlProduto = new CtrlProduto();
            CtrlFornecedor ctrlfornecedor = new CtrlFornecedor();
            CtrlOrdemServico ctrlOrdemServico = new CtrlOrdemServico();
            CtrlVeiculo ctrlVeiculo = new CtrlVeiculo();
            CtrlCliente ctrlCliente = new CtrlCliente();

            for(int i = 0; i < pecas_adicionadas; i++)
            {
                try
                {
                    Connection con = (Connection) Conexao.getConnection();

                    Statement stm = (Statement) con.createStatement();

                    String nome_produto = Lista_Pecas.getModel().getElementAt(i).toString();

                    ResultSet rsProduto = ctrlProduto.consultarPorNome(stm, nome_produto);

                    int idFornecedor = 0;

                    int idProduto = 0;

                    while(rsProduto.next())
                    {
                        idFornecedor = Integer.parseInt(rsProduto.getString("idFornecedor"));

                        idProduto = Integer.parseInt(rsProduto.getString("idProduto"));

                        ctrlProduto.atualizaEstoque(stm, idProduto, idFornecedor);
                    }
                }
                catch (Exception e) { }
            }

            try
            {
                Connection con = (Connection) Conexao.getConnection();

                Statement stm = (Statement) con.createStatement();

                String placa = jComboBox2.getSelectedItem().toString();
            
                ResultSet rsVeiculo = ctrlVeiculo.consultar(stm, placa);

                int idCliente = 0;
                
                int idVeiculo = 0;
                
                
                while(rsVeiculo.next())
                {
                    idVeiculo = Integer.parseInt(rsVeiculo.getString("idVeiculo"));
                    
                    idCliente = Integer.parseInt(rsVeiculo.getString("idCliente"));
                }
                ctrlOrdemServico.adicionar(stm, Descricao.getText(), ctrlOrdemServico.converteData(jTextField3.getText()), idCliente, idVeiculo, placa);
            
                JOptionPane.showMessageDialog(this, "Ordem de Servico Efetuada!");
            }
            catch (Exception e) { }
            
            this.setVisible(false);
        }
        else
            JOptionPane.showMessageDialog(this, "Operacão não realizada!\nNecessário selecionar Veículo e Cliente!");
    }//GEN-LAST:event_SalvarActionPerformed

