    private void BuscarPecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarPecaActionPerformed
        // TODO add your handling code here:
        int quantidade_produto = 0;
        
        try
        {
            CtrlProduto ctrlProduto = new CtrlProduto();
            
            CtrlFornecedor ctrlfornecedor = new CtrlFornecedor();
        
            Connection con = (Connection) Conexao.getConnection();
        
            Statement stm = (Statement) con.createStatement();
        
            String nome_produto = jComboBox3.getSelectedItem().toString();
            
            ResultSet rsProduto = ctrlProduto.consultarPorNome(stm, nome_produto);
            
            int idFornecedor = 0;
            
            int idProduto = 0;
            
            while(rsProduto.next())
            {
                idFornecedor = Integer.parseInt(rsProduto.getString("idFornecedor"));
                
                idProduto = Integer.parseInt(rsProduto.getString("idProduto"));
                
                quantidade_produto = Integer.parseInt(rsProduto.getString("quantidade"));
                
                preco_custo += Double.parseDouble(rsProduto.getString("valor_custo"));
            }
            PrecoCusto.setText("" + preco_custo);
        }
        catch (Exception e) { }
        
        if(quantidade_produto > 0)
        {
            listaProdutosOrdemServico.add(jComboBox3.getSelectedItem().toString());
        
            Lista_Pecas.setModel(new DefaultComboBoxModel(new Vector<String>(listaProdutosOrdemServico)));
        }
        else
            JOptionPane.showMessageDialog(this, "Peca Desejada não tem o suficiente");
        
        pecas_adicionadas++;
    }//GEN-LAST:event_BuscarPecaActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try
        {
            CtrlProduto ctrlProduto = new CtrlProduto();
            
            CtrlFornecedor ctrlfornecedor = new CtrlFornecedor();
        
            Connection con = (Connection) Conexao.getConnection();
        
            Statement stm = (Statement) con.createStatement();
        
            String nome_produto = (String)Lista_Pecas.getSelectedValue();
            
            ResultSet rsProduto = ctrlProduto.consultarPorNome(stm, nome_produto);
            
            int idFornecedor = 0;
            
            int idProduto = 0;
            
            while(rsProduto.next())
            {
                idFornecedor = Integer.parseInt(rsProduto.getString("idFornecedor"));
                
                idProduto = Integer.parseInt(rsProduto.getString("idProduto"));
                
                preco_custo -= Double.parseDouble(rsProduto.getString("valor_custo"));
            }
            PrecoCusto.setText("" + preco_custo);
        }
        catch (Exception e) { }
     
        listaProdutosOrdemServico.remove(Lista_Pecas.getSelectedIndex());
        
        Lista_Pecas.setModel(new DefaultComboBoxModel(new Vector<String>(listaProdutosOrdemServico)));
        
        pecas_adicionadas--;
    }//GEN-LAST:event_jButton2ActionPerformed

